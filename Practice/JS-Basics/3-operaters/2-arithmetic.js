let x = 10;
let y = 3;

// Addition
console.log(x + y);

// Subraction
console.log(x - y);

// Multiplication
console.log(x * y);

// Division
console.log(x / y);

// Remainder of division
console.log(x % y);

// Exponentiation
console.log(x ** y);

// Increment
console.log(++x);

console.log(x++);
console.log(x);

// Decrement
console.log(--x);

console.log(x--);
console.log(x);